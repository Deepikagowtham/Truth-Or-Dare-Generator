public class Truth {

        public void truthQuestion(int ch){
            switch (ch) {
                case 1:
                    System.out.println("What is the most embarrassing thing that has ever happened to you in public?");
                    break;
                case 2:
                    System.out.println("Have you ever lied to get out of trouble? What was the situation?");
                    break;
                case 3:
                    System.out.println("What is the silliest fear you have?");
                    break;
                case 4:
                    System.out.println("Have you ever snooped through someone else's phone or computer without their permission?");
                    break;
                case 5:
                    System.out.println("What's the most awkward date you've ever been on?");
                    break;
                case 6:
                    System.out.println("Have you ever cheated on a test or exam? If so, how?");
                    break;
                case 7:
                    System.out.println("What's the most embarrassing thing you've done in front of your crush?");
                    break;
                case 8:
                    System.out.println("Have you ever pretended to like a gift you didn't actually like? What was it?");
                    break;
                case 9:
                    System.out.println("What's the biggest secret you've kept from your parents?");
                    break;
                case 10:
                    System.out.println("Have you ever stolen something? If so, what was it and why?");
                    break;
                default:
                    break;
            }
        }
}

